// ============================================
// ЗАДАЧА: Здороваемся по имени
// ============================================
// Спроси у пользователя его имя через prompt.
// Если имя введено - покажи приветствие через alert.
// Если имя пустое или пользователь нажал Отмену - покажи "Введено неверное имя."
//
// Все сообщения должны выводиться через alert (модальные окна).
//
// Это интерактивная страничка - решение запускается по кнопке "Запустить".

// ============================================
// ПОДСКАЗКИ (что использовать):
// ============================================
//
// 1. prompt("Текст вопроса")
//    - открывает окошко с полем ввода
//    - возвращает то, что ввел пользователь (строка)
//    - если пользователь нажал "Отмена" - возвращает null
//    - если ничего не ввел и нажал OK - возвращает "" (пустая строка)
//
// 2. alert("Текст")
//    - открывает окошко с сообщением
//
// 3. Проверка "имя пустое или null":
//    - Можно через if (name) { ... } - сработает для null и ""
//      (потому что оба - falsy!)
//    - Если хочешь учесть пробелы " " - используй name.trim()
//
// 4. Template literal для приветствия:
//    `Привет, ${name}!`

// ============================================
// ТВОЕ РЕШЕНИЕ - пиши внутри функции greetUser
// ============================================

function greetUser() {
  const userName = prompt("Как тебя зовут?");
  if (userName) {
    alert(`Привет, ${userName}!`);
  } else {
    alert("Введено неверное имя.");
  }
}

// Эта строка привязывает функцию к кнопке "Запустить" - не трогай.
document.getElementById("startBtn").addEventListener("click", greetUser);
